    public void getAllRecords(String email){
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        dtm.setRowCount(0);
        ArrayList<User> list = UserDao.getAllRecordsWithStatusFalse(email);
        Iterator<User> itr = list.iterator();
        while(itr.hasNext()){
            User userObj = itr.next();
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            String dateBirthday = sdf.format(userObj.getDateBirth());
            String dateStart = sdf.format(userObj.getDateStart());
            if(!userObj.getEmail().equals("admin@gmail.com")){
                dtm.addRow(new Object[]{userObj.getId(), userObj.getName(), userObj.getEmail(), userObj.getMobileNumber(), userObj.getAddress(), userObj.getCCCD(), dateBirthday, dateStart, userObj.getDepartment_id(), userObj.getSecurityQuestion(),userObj.getStatus()});
                
            }
        }
    }
